    private void changePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordButtonActionPerformed
        try {
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/biblioteca", "root", "");
            PreparedStatement pst = con.prepareStatement("UPDATE utilizatori SET password = ? WHERE name = ?");
            pst.setString(1, String.valueOf(passwordField.getPassword()));
            pst.setString(2, usernameField.getText());
            if(String.valueOf(passwordField.getPassword()).matches(String.valueOf(repeatPasswordField.getPassword()))){
                int rows = pst.executeUpdate();
                if(rows > 0){
                    JOptionPane.showMessageDialog(this, "Password Changed!");
                    LogIn login = new LogIn();
                    login.setVisible(true);
                    this.dispose();
                }
                else{
                    JOptionPane.showMessageDialog(this, "Password Change Unsuccessful!");
                }
            }
            else{
                JOptionPane.showMessageDialog(this, "Passwords don't match!");
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_changePasswordButtonActionPerformed

